# Integer 
# Tam sayılar için kullanılan veri tipidir.
1
10
100
100_100_100_2

print(123 + 456)

# Ek bilgi 
# Büyük sayıların daha okunaklı olması için tam sayıların arasına _ kullanılabilir.

print(123_456_789)


# Float 
# Ondalıklı sayılar için kullanılan veri tipidir.

print(3.14159)

# Boolean
# Yanlızca iki seçeneği olan veri tipidir. Yanlızca True ve False değerini alır. Bunun dışında farklı değer alamaz. 
